export function ListingPost(
  title,
  name,
  email,
  phoneNum,
  startDate,
  endDate,
  rent,
  neighborhood,
  addr,
  numMates,
  mateGender,
  bedStatus,
  bathStatus,
  other
){
  this.title = title;
  this.name = name;
  this.email = email;
  this.phoneNum = phoneNum;
  this.startDate = startDate;
  this.endDate = endDate;
  this.rent = rent;
  this.neighborhood = neighborhood;
  this.addr = addr;
  this.numMates = numMates;
  this.mateGender = mateGender;
  this.bedStatus = bedStatus;
  this.bathStatus = bathStatus;
  this.other = other;
}